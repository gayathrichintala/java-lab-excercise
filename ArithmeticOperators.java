package day3;

public class ArithmeticOperators {


		    public static void main(String[] args) {
		        // Addition
		        int sum = 10 + 5; // sum is 15
		        
		        // Subtraction
		        int difference = 10 - 5; // difference is 5
		        
		        // Multiplication
		        int product = 10 * 5; // product is 50
		        
		        // Division
		        int quotient1 = 10 / 3; // quotient1 is 3 (integer division, remainder discarded)
		        double quotient2 = 10.0 / 3; // quotient2 is 3.3333333333333335 (floating-point division)
		        
		        // Modulus
		        int remainder = 10 % 3; // remainder is 1 (remainder of 10 divided by 3 is 1)
		               
		        
	}

}
