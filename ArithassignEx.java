package day3;

public class ArithassignEx {

	public static void main(String[] args) {
		int a=10,b=3;
		a+=b;//a-a+b
		System.out.println(a);
	    a-=b;//a-a-b
		System.out.println(a);
		a*=b;//a-a*b
		System.out.println(a);
		a/=b;//a-a/b
		System.out.println(a);
		a%=b;//a-a%b
		System.out.println(a);

	}

}
